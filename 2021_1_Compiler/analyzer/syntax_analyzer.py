class Stack():
    def __init__(self):
        self.top = []

    def __len__(self):
        return len(self.top)

    def __str__(self):
        return str(self.top[::1])

    def isEmpty(self):
        return len(self.top) == 0

    def push(self, item):
        if item != None:
            self.top.append(item)
        else:
            pass

    def pop(self):
        if not self.isEmpty():
            return self.top.pop(-1)
        else:
            print("Syntax Analyzer : Error-stack underflow")
            exit()

    def isContain(self, item):
        return item in self.top

    def peek(self):
        if not self.isEmpty():
            return self.top[-1]
        else:
            print("Syntax Analyzer : Error-underflow")
            exit()

    def size(self):
        return len(self.top)


