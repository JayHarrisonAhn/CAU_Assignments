def main(input):
    f_in = open(input, 'rt')
    line = f_in.readline()
    token = []
    lines = []
    while line != '':
        li = line.split('\t', maxsplit=1)
        token.append(li[0])
        line = f_in.readline()
        lines.append(line)

    f_in.close()
    token.append('$')

    state_stack = Stack()
    grammer_stack = Stack()
    i = 0
    state_stack.push(0)
    while True:
        key = SLRtable.table[state_stack.peek()][token[i]]
        if key == None:
            print("Syntax Analyzer : Failed")
            print(
                f"Syntax Analyzer : {i}'s token {grammer_stack.peek()} can't choice {token[i]}")

            break
        if key == 'acc':
            print(f"Syntax Analyzer : Accepted")
            break
        order = key[0]
        state = int(key[1:])
        if order == 's':
            state_stack.push(state)
            grammer_stack.push(token[i])
            i += 1
        elif order == 'r':
            grammer = Grammer.CFG[state]
            for _ in range(grammer[-1]):
                state_stack.pop()
                grammer_stack.pop()
            for item in grammer[:-1]:
                grammer_stack.push(item)
            state_stack.push(
                int(SLRtable.table[state_stack.peek()][grammer_stack.peek()]))


if __name__ == "__main__":
    main(sys.argv[1])
